        private List<string> GenerateNamesMultiplication(List<string> surnames, List<string> names, Dictionary<string, string> patronymics)
        {
            List<string> rslt = new List<string>();
            foreach (string surname in surnames)
            {
                foreach (string name in names)
                {
                    foreach(string patronymic in patronymics.Values)
                    {
                        rslt.Add(string.Format("{0} {1} {2}", surname.Trim(), name.Trim(), patronymic.Trim()));
                    }
                }
            }
            return rslt;
        }

